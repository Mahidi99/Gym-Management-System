package sample;

//representing membership start date
public class Date {

    //Day of membership started
    private int Membership_Start_Day;
    //Month of membership started
    private int Membership_Start_Month;
    //Year of membership started
    private int Membership_Start_Year;

    public Date(int Membership_Start_Day, int Membership_Start_Month, int Membership_Start_Year){
        super();
        //Day of membership started
        this.Membership_Start_Day = Membership_Start_Day;
        //Month of membership started
        this.Membership_Start_Month = Membership_Start_Month;
        //Year of membership started
        this.Membership_Start_Year = Membership_Start_Year;
    }
}
